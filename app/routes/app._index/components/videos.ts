function randomDateThisMonth() {
    const now = new Date();
    const start = new Date(now.getFullYear(), now.getMonth(), 1);
    const end = now;
    return new Date(start.getTime() + Math.random() * (end.getTime() - start.getTime())).toISOString();
}

